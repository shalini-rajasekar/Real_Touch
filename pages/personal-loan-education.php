<script>
// ========== EMI CALCULATOR FUNCTIONS ==========

// Sync loan amount between input and range slider
function syncLoanAmount(value) {
  document.getElementById('ple-loan-amount').value = value;
  calculateEducationEMI();
}

// Sync interest rate between input and range slider
function syncInterestRate(value) {
  document.getElementById('ple-interest-rate').value = value;
  calculateEducationEMI();
}

// Sync tenure between input and range slider
function syncTenure(value) {
  document.getElementById('ple-tenure').value = value;
  calculateEducationEMI();
}

// Main EMI calculation function
function calculateEducationEMI() {
  // Get input values
  const principal = parseFloat(document.getElementById('ple-loan-amount').value);
  const annualRate = parseFloat(document.getElementById('ple-interest-rate').value);
  const tenureYears = parseFloat(document.getElementById('ple-tenure').value);
  
  // Validate inputs
  if (!principal || !annualRate || !tenureYears || principal <= 0 || annualRate <= 0 || tenureYears <= 0) {
    return;
  }
  
  // Sync range sliders
  document.getElementById('ple-loan-range').value = principal;
  document.getElementById('ple-rate-range').value = annualRate;
  document.getElementById('ple-tenure-range').value = tenureYears;
  
  // Calculate EMI
  const monthlyRate = annualRate / 12 / 100;
  const tenureMonths = tenureYears * 12;
  
  // EMI Formula: P × r × (1 + r)^n / ((1 + r)^n - 1)
  const emi = principal * monthlyRate * Math.pow(1 + monthlyRate, tenureMonths) / 
              (Math.pow(1 + monthlyRate, tenureMonths) - 1);
  
  const totalAmount = emi * tenureMonths;
  const totalInterest = totalAmount - principal;
  
  // Format and display results
  document.getElementById('ple-emi-result').textContent = '₹' + formatNumber(emi.toFixed(0));
  document.getElementById('ple-principal-result').textContent = '₹' + formatNumber(principal.toFixed(0));
  document.getElementById('ple-interest-result').textContent = '₹' + formatNumber(totalInterest.toFixed(0));
  document.getElementById('ple-total-result').textContent = '₹' + formatNumber(totalAmount.toFixed(0));
}

// Format numbers with Indian comma style
function formatNumber(num) {
  return num.toString().replace(/\B(?=(\d{3})+(?!\d))/g, ',');
}

// ========== FAQ TOGGLE FUNCTION ==========
function toggleEducationFAQ(button) {
  const faqItem = button.parentElement;
  const answer = faqItem.querySelector('.ple-faq-answer');
  const isActive = faqItem.classList.contains('active');
  
  // Close all FAQ items
  document.querySelectorAll('.ple-faq-item').forEach(item => {
    item.classList.remove('active');
    item.querySelector('.ple-faq-answer').style.maxHeight = '0';
  });
  
  // Open clicked FAQ if it wasn't active
  if (!isActive) {
    faqItem.classList.add('active');
    answer.style.maxHeight = answer.scrollHeight + 'px';
  }
}

// ========== ELIGIBILITY TAB SWITCHING ==========
function switchEligibilityTab(tabName) {
  // Remove active class from all tabs and content
  document.querySelectorAll('.ple-tab').forEach(tab => tab.classList.remove('active'));
  document.querySelectorAll('.ple-tab-content').forEach(content => content.classList.remove('active'));
  
  // Add active class to selected tab and content
  document.querySelector(`[data-tab="${tabName}"]`).classList.add('active');
  document.getElementById(tabName).classList.add('active');
}

