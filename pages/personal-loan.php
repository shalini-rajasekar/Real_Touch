<script>
// EMI Calculator Function
function calculateEMI() {
  const principal = parseFloat(document.getElementById('loanAmount').value);
  const annualRate = parseFloat(document.getElementById('interestRate').value);
  const tenureMonths = parseFloat(document.getElementById('tenure').value);
  
  if (!principal || !annualRate || !tenureMonths) {
    alert('Please fill all fields');
    return;
  }
  
  // Calculate monthly interest rate
  const monthlyRate = annualRate / 12 / 100;
  
  // EMI Formula: P × r × (1 + r)^n / ((1 + r)^n - 1)
  const emi = principal * monthlyRate * Math.pow(1 + monthlyRate, tenureMonths) / 
              (Math.pow(1 + monthlyRate, tenureMonths) - 1);
  
  const totalAmount = emi * tenureMonths;
  const totalInterest = totalAmount - principal;
  
  // Display results
  document.getElementById('emiResult').textContent = emi.toFixed(2).replace(/\B(?=(\d{3})+(?!\d))/g, ',');
  document.getElementById('interestResult').textContent = totalInterest.toFixed(2).replace(/\B(?=(\d{3})+(?!\d))/g, ',');
  document.getElementById('totalResult').textContent = totalAmount.toFixed(2).replace(/\B(?=(\d{3})+(?!\d))/g, ',');
  
  // Show result box
  document.getElementById('resultBox').classList.add('active');
}

// FAQ Toggle Function
function toggleFAQ(button) {
  const faqItem = button.parentElement;
  const answer = faqItem.querySelector('.pl-faq-answer');
  const isActive = faqItem.classList.contains('active');
  
  // Close all FAQs
  document.querySelectorAll('.pl-faq-item').forEach(item => {
    item.classList.remove('active');
    item.querySelector('.pl-faq-answer').style.maxHeight = '0';
  });
  
  // Open clicked FAQ if it wasn't active
  if (!isActive) {
    faqItem.classList.add('active');
    answer.style.maxHeight = answer.scrollHeight + 'px';
  }
}

// Allow Enter key to calculate EMI
document.querySelectorAll('.pl-input-group input').forEach(input => {
  input.addEventListener('keypress', function(e) {
    if (e.key === 'Enter') {
      calculateEMI();
    }
  });
});

